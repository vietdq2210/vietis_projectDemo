
public class StatusDTO {
    private Integer id;
    private String name;
    private Integer statusClassificationId;

    public Integer getId() {
        return this.id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getStatusClassificationId() {
        return this.statusClassificationId;
    }

    public void setStatusClassificationId(Integer statusClassificationId) {
        this.statusClassificationId = statusClassificationId;
    }
}
